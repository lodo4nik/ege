import sys
# Установим лимит рекурсии повыше для больших файлов (хотя в данной задаче это не критично)
# sys.setrecursionlimit(2000)

def solve_task(filename="C:/Users/dog/Documents/GitHub/ege/24.txt"):
    """
    Находит максимальную длину последовательности символов в файле, 
    удовлетворяющей условиям: 
    1. 'F' встречается ровно 76 раз.
    2. Четная цифра встречается ровно 1 раз.
    3. Последовательность начинается с этой четной цифры.
    """
    
    # 1. Чтение файла
    try:
        # Убедитесь, что имя файла соответствует тому, что у вас есть.
        # В данном примере используется условное имя "24-4.txt"
        with open(filename, 'r') as file:
            data = file.read().strip()
    except FileNotFoundError:
        print(f"Ошибка: Файл '{filename}' не найден. Пожалуйста, убедитесь, что файл находится в той же папке.")
        return 0
    except Exception as e:
        print(f"Произошла ошибка при чтении файла: {e}")
        return 0

    N = len(data)
    MAX_F_COUNT = 76
    max_length = 0

    # 2. Предварительная обработка: Сбор индексов
    
    # Функция для проверки, является ли символ четной цифрой
    def is_even_digit(char):
        return char in '02468'

    # Список для хранения индексов всех четных цифр
    even_digit_indices = [i for i, char in enumerate(data) if is_even_digit(char)]
    
    # Список для хранения индексов всех букв 'F'
    f_indices = [i for i, char in enumerate(data) if char == 'F']
    # Добавляем фиктивный индекс в конец, чтобы упростить логику поиска. 
    # Это индекс, который находится "за пределами" строки.
    f_indices.append(N) 

    # 3. Основной алгоритм: Итерация по всем возможным начальным точкам
    
    # Последовательность должна начинаться с ЕДИНСТВЕННОЙ четной цифры.
    # Это означает, что каждая четная цифра в even_digit_indices является потенциальным 
    # началом искомой последовательности.
    
    # Итерация по индексам всех четных цифр (возможные начальные точки 'start')
    for start_index in even_digit_indices:
        
        # Шаг 3.1: Найти индекс, где будет ровно 76 букв 'F'
        
        # Если количество F's, начиная с текущей позиции, меньше 76, 
        # то нет смысла продолжать для этой начальной точки.
        
        # Находим первый индекс F, который находится СТРОГО ПОСЛЕ start_index.
        # 'start_f_index' - индекс F, который будет ПЕРВОЙ F в последовательности.
        # Если start_f_index не найден, значит F's после start_index нет.
        
        start_f_index = -1
        for i, f_idx in enumerate(f_indices):
            if f_idx > start_index:
                start_f_index = i
                break
        
        # Если даже первой F после начала нет, или их недостаточно для 76, пропускаем.
        if start_f_index == -1:
             continue # F's не найдено после start_index
        
        # 3.2: Проверка наличия 76 букв 'F'
        
        # f_indices[start_f_index] - индекс 1-й F в искомой последовательности
        # f_indices[start_f_index + MAX_F_COUNT - 1] - индекс 76-й F в искомой последовательности
        
        # Индекс 76-й буквы 'F' в полном списке f_indices (0-индексация)
        index_76th_f = start_f_index + MAX_F_COUNT - 1
        
        # Проверяем, существует ли 76-я буква 'F' в списке f_indices
        if index_76th_f >= len(f_indices) - 1: # -1 из-за фиктивного N в конце
            # Недостаточно букв 'F' до конца файла
            continue
            
        # Индекс 76-й F в файле
        end_f_index = f_indices[index_76th_f]
        
        # Шаг 3.3: Найти индекс, где будет ровно 2 четные цифры
        # (Искомая последовательность должна содержать ТОЛЬКО ОДНУ четную цифру,
        # которая является ее началом).
        
        # Находим первый индекс четной цифры, который находится СТРОГО ПОСЛЕ start_index.
        
        # 'end_even_index' - это индекс ВТОРОЙ четной цифры в тексте,
        # которая не должна быть включена в нашу последовательность.
        
        end_even_index = N
        
        # Ищем индекс первой четной цифры после start_index
        for even_idx in even_digit_indices:
            if even_idx > start_index:
                end_even_index = even_idx
                break
        
        # Шаг 3.4: Определить границы и длину
        
        # Верхняя граница последовательности (индекс СЛЕДУЮЩЕГО за концом символа)
        # 1. Последовательность должна включать 76-ю F (end_f_index). 
        #    Значит, длина должна быть как минимум end_f_index - start_index + 1.
        # 2. Последовательность НЕ ДОЛЖНА включать 2-ю четную цифру (end_even_index).
        #    Значит, длина должна быть максимум end_even_index - start_index.
        
        # Поиск максимальной длины:
        # Конец последовательности (индекс, следующий за последним символом)
        # Максимально допустимый конец: 
        # - Индекс 76-й F + 1
        # - Индекс 2-й четной цифры
        
        # Последний допустимый индекс в последовательности - это минимум из:
        # - end_even_index - 1 (так как end_even_index не включается)
        # - N - 1 (конец файла)
        
        # Индекс, следующий за концом 76-й F
        end_after_76th_f = end_f_index + 1
        
        # Максимально допустимый конец (индекс СЛЕДУЮЩЕГО за концом символа)
        end = min(end_even_index, end_after_76th_f)
        
        # Длина последовательности
        current_length = end - start_index
        
        # Обновление максимальной длины
        if current_length > 0:
            max_length = max(max_length, current_length)

    # 4. Вывод результата
    print(f"Максимальное количество символов в найденной последовательности: {max_length}")
    return max_length

# Вызов функции с условным именем файла
